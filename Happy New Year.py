import random
import time
import sys

def typewriter_effect(text, delay=0.05):
    for char in text:
        sys.stdout.write(char)
        sys.stdout.flush()
        time.sleep(delay)

def erase_effect(text, erase_delay=0.02):
    for _ in text:
        sys.stdout.write("\b \b")  
        sys.stdout.flush()
        time.sleep(erase_delay)

